def search_faq(query: str) -> list:
    """
    –ü–æ–∏—Å–∫ –≤–æ–ø—Ä–æ—Å–æ–≤ –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º

    Returns:
        –°–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤ [(category, q_id, question_data), ...]
    """
    query_lower = query.lower()
    results = []

    for category_id, category_data in FAQ_DATABASE.items():
        for q_id, q_data in category_data["questions"].items():
            # –ü–æ–∏—Å–∫ –≤ –≤–æ–ø—Ä–æ—Å–µ
            if query_lower in q_data["q"].lower():
                results.append((category_id, q_id, q_data))
                continue

            # –ü–æ–∏—Å–∫ –≤ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤–∞—Ö
            if any(query_lower in kw.lower() for kw in q_data["keywords"]):
                results.append((category_id, q_id, q_data))

    return results[:10]  # –ú–∞–∫—Å–∏–º—É–º 10 —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤


def get_category_questions(category_id: str) -> list:
    """–ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ –≤–æ–ø—Ä–æ—Å—ã –∫–∞—Ç–µ–≥–æ—Ä–∏–∏"""
    if category_id not in FAQ_DATABASE:
        return []

    category = FAQ_DATABASE[category_id]
    questions = []

    for q_id, q_data in category["questions"].items():
        questions.append((q_id, q_data))

    return questions


def get_total_faq_count() -> int:
    """–ü–æ–ª—É—á–∏—Ç—å –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–ø—Ä–æ—Å–æ–≤ –≤ FAQ"""
    total = 0
    for category in FAQ_DATABASE.values():
        total += len(category["questions"])
    return total


